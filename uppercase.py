# Ask user to enter their first name
first_name = input("What is your first name? ")

# Ask user to enter their last name
last_name = input("What is your last name? ")

# Combine first and last name with a space in between
full_name = f"{first_name} {last_name}"

# Convert the full name to uppercase
result = full_name.upper()

# Print the result
print(f"Your name is {result}")

